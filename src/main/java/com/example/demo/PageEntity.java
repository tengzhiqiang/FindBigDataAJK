package com.example.demo;

public class PageEntity {
	
	private String title;
	private String herf;  //详细界面
	private String hoursetype;  //房屋类型
	private String area;  //房屋类型
	private String year;  //房屋建设年份
	
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getHerf() {
		return herf;
	}
	public void setHerf(String herf) {
		this.herf = herf;
	}
	public String getHoursetype() {
		return hoursetype;
	}
	public void setHoursetype(String hoursetype) {
		this.hoursetype = hoursetype;
	}
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	
	
	
	

}
